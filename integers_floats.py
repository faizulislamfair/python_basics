# Arithmetic Operators :
# Addition:          7 + 5
# Subtraction:       7 - 5
# Multiplication:    7 * 5
# Division:          7 / 5
# Floor Division:    7 // 5
# Exponent:          7 ** 5
# Modulus:           7 % 5

print(7 // 5)

print(7 ** 5)

print(abs(-5))

print(round(5.75, 1))


# Comparisons:
# Equal:           7 == 5
# Not Equal:       7 != 5
# Greater Than:    7 > 5
# Less Than:       7 < 5
# Greater or Equal 7 >= 5
# Less or Equal    7 <= 5

num_1 = 7
num_2 = 5

print(num_1 >= num_2)


# Strings

num_3 = '500'
num_4 = '500'

print(num_3 + num_4)


